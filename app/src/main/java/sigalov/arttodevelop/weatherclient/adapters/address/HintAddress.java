package sigalov.arttodevelop.weatherclient.adapters.address;


public class HintAddress
{
    private int id;
    private String name;

    public HintAddress(int id, String name)
    {
        this.id = id;
        this.name = name;

    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
